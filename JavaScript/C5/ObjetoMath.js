// Potencia
Math.pow(2, 3);
// 2x2x2 = 8

// Redondeo
Math.round(0.49)
// Redondea al  enterno más próximo en este caso a 0, pero con + 0.50 redondea a 1

Math.floor(5.93)
// Redondea al entero de menos valor, en este caso a 5

Math.ceil(3.27)
// Redondea al entero de mayor valor, en este caso a 4

// Mínimo y máximo de un conjunto de datos
Math.min(1, 2, 3, 4, 5, 6, 7)
// 1

Math.max(1, 2, 3, 4, 5, 6, 7)
// 7

// Número random
Math.random();
// genera un número decimal random entre el 1 y el 0